void cod(char a){
  b=0b10000000;
  while(!(b&a))b>>=1;
  while(b>>=1){
    LIGAR;
    if(b&a)DELAY(2*TICK);
    DELAY(TICK);
    DESLIGAR;
    DELAY(TICK);
  }
DELAY(3*TICK);
}

